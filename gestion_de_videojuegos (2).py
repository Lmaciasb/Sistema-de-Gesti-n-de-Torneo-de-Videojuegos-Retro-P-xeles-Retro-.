def ejecutar_ronda_1():
    imprimir_titulo("RONDA 1: FASE REGULAR (3 PARTIDAS)")
    nombres_equipos = list(equipos_db.keys())

    if len(nombres_equipos) < 2:
        print("Error: Se necesitan al menos 2 equipos para iniciar el torneo.")
        return False

    # Ejecutamos 3 fechas
    for fecha in range(1, 4):
        imprimir_info(f"Generando enfrentamientos FECHA {fecha}...")
        random.shuffle(nombres_equipos)

        jugados_esta_fecha = set()

        # Algoritmo simple de emparejamiento
        for i in range(len(nombres_equipos)):
            eq1 = nombres_equipos[i]

            if eq1 in jugados_esta_fecha:
                continue

            # Buscamos un rival disponible que no sea él mismo y no hayan jugado antes
            rival_encontrado = None
            for j in range(i + 1, len(nombres_equipos)):
                eq2 = nombres_equipos[j]
                par = tuple(sorted((eq1, eq2)))

                if eq2 not in jugados_esta_fecha and par not in HISTORIAL_PARTIDAS:
                    rival_encontrado = eq2
                    break

            if rival_encontrado:
                registrar_resultado(eq1, rival_encontrado, f"R1-Fecha {fecha}")
                jugados_esta_fecha.add(eq1)
                jugados_esta_fecha.add(rival_encontrado)
            else:
                print(f"   (Nota: {eq1} queda libre en esta fecha por falta de rival compatible)")

        guardar_puntajes()
        time.sleep(1)

    imprimir_exito("Ronda 1 finalizada.")
    mostrar_tabla_posiciones("TABLA POST RONDA 1")
    return True

