def solicitar_dato_validado(mensaje, tipo_dato, min_val=None, max_val=None):
    while True:
        try:
            entrada = input(mensaje)

            if tipo_dato == str:
                if entrada.strip() == "":
                    imprimir_error("El campo no puede estar vacío.")
                    continue
                return entrada.strip()

            valor = tipo_dato(entrada)

            if min_val is not None and max_val is not None:
                if not (min_val <= valor <= max_val):
                    imprimir_error(f"El valor debe estar entre {min_val} y {max_val}.")
                    continue

            return valor

        except ValueError:
            imprimir_error("Tipo de dato inválido. Ingrese un número entero.")

