def registrar_resultado(equipo_a, equipo_b, etapa):
    """
    Gestiona la interacción con el usuario para definir el ganador.
    """
    print(f"\n>>> {etapa}: \033[1m{equipo_a}\033[0m vs \033[1m{equipo_b}\033[0m")
    print("   [1] Ganó " + equipo_a)
    print("   [2] Ganó " + equipo_b)
    print("   [3] Empate")

    while True:
        opcion = input("   Resultado: ").strip()
        if opcion in ['1', '2', '3']:
            break
        print("   Opción inválida. Ingrese 1, 2 o 3.")

    pts_a = 0
    pts_b = 0

    if opcion == '1':
        pts_a = 3
        print(f"   -> Ganador: {equipo_a} (+3 pts)")
    elif opcion == '2':
        pts_b = 3
        print(f"   -> Ganador: {equipo_b} (+3 pts)")
    else:
        pts_a = 1
        pts_b = 1
        print(f"   -> Empate (+1 pt c/u)")

    # Actualizar Base de Datos
    equipos_db[equipo_a]['puntos'] += pts_a
    equipos_db[equipo_a]['partidas_jugadas'] += 1

    equipos_db[equipo_b]['puntos'] += pts_b
    equipos_db[equipo_b]['partidas_jugadas'] += 1

    # Registramos el enfrentamiento en historial global para no repetir en R1
    pareja_ordenada = tuple(sorted((equipo_a, equipo_b)))
