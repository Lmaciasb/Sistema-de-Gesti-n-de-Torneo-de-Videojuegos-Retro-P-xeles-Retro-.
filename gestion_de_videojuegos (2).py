def registrar_participante():
    print("\n" + "="*40)
    print("--- NUEVO REGISTRO (Escriba 'X' para finalizar y guardar) ---")

    # 1. Datos del Primer Participante
    nombre_1 = solicitar_nombre_participante_unico("Nombre Completo 1 (3 palabras): ")
    if nombre_1 is None:
        return False # Salir

    edad_1 = solicitar_dato_validado("Edad (12-70): ", int, 12, 70)
    nivel_1 = solicitar_dato_validado("Nivel (1-5): ", int, 1, 5)

    p1 = {"nombre": nombre_1, "edad": edad_1, "nivel": nivel_1}

    # 2. Consulta sobre Equipo
    while True:
        tiene_equipo = input("\n¿Este jugador ya tiene un equipo formado? (si/no): ").strip().lower()
        if tiene_equipo in ['si', 'no']:
            break
        imprimir_error("Por favor responda 'si' o 'no'.")

    # --- CASO A: NO TIENE EQUIPO ---
    if tiene_equipo == 'no':
        participantes_db.append(p1)
        imprimir_exito(f"GUARDADO EN MEMORIA: {nombre_1} (Lista de espera).")
        print(">> Reseteando formulario...")
        return True

    # --- CASO B: SÍ TIENE EQUIPO ---
    else:
        print("\n>> Registro del Equipo y Compañero:")

        while True:
            nombre_equipo = input("Ingrese Nombre del Equipo: ").strip()
            if nombre_equipo == "":
                imprimir_error("El nombre del equipo no puede estar vacío.")
            elif nombre_equipo in equipos_db:
                imprimir_error("Ese nombre de equipo ya existe en la base de datos.")
            else:
                break

        while True:
            nombre_2 = solicitar_nombre_participante_unico("Nombre Completo 2 (3 palabras): ")
            if nombre_2 is None:
                imprimir_error("No puede salir a mitad de un registro de equipo. Complete el nombre.")
                continue

            if nombre_2.lower() == nombre_1.lower():
                imprimir_error("El compañero no puede ser la misma persona.")
                continue
            break

        edad_2 = solicitar_dato_validado("Edad (12-70): ", int, 12, 70)
        nivel_2 = solicitar_dato_validado("Nivel (1-5): ", int, 1, 5)

        p2 = {"nombre": nombre_2, "edad": edad_2, "nivel": nivel_2}

        # Guardado en memoria local
        participantes_db.append(p1)
        participantes_db.append(p2)

        equipos_db[nombre_equipo] = {
            'miembros': [p1, p2],
            'puntos': 0,
            'partidas_jugadas': 0
        }

        jugadores_ocupados.add(nombre_1)
        jugadores_ocupados.add(nombre_2)

        imprimir_exito(f"GUARDADO EN MEMORIA: Equipo '{nombre_equipo}' con {nombre_1} y {nombre_2}.")
