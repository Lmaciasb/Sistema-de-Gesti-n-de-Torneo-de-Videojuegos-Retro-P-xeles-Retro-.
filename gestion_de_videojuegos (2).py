def asignar_nombres_definitivos_autoformados():
    imprimir_titulo("RENOMBRAMIENTO DE EQUIPOS AUTO-FORMADOS")

    # 1. Identificar equipos que necesitan cambio de nombre
    # Buscamos equipos que tengan la marca 'origen': 'automatico' (del Bloque 2)
    equipos_a_renombrar = []
    for nombre, datos in equipos_db.items():
        if datos.get('origen') == 'automatico':
            equipos_a_renombrar.append(nombre)

    if not equipos_a_renombrar:
        imprimir_aviso("No se encontraron equipos automáticos para renombrar.")
        return

    # 2. Validar que tenemos suficientes palabras clave
    if len(equipos_a_renombrar) > len(PALABRAS_CLAVE):
        imprimir_error(f"Hay más equipos automáticos ({len(equipos_a_renombrar)}) que palabras disponibles ({len(PALABRAS_CLAVE)}).")
        print("Agregue más palabras a la lista 'PALABRAS_CLAVE' en el código.")
        return

    # 3. Mezclar aleatoriamente las palabras para que la asignación sea al azar
    random.shuffle(PALABRAS_CLAVE)

    cambios_realizados = 0

    # 4. Proceso de Renombramiento
    # Nota: No podemos cambiar la clave de un diccionario mientras lo iteramos.
    # Por eso recolectamos los cambios primero y los aplicamos después.
    nuevos_nombres_map = {} # {nombre_viejo: nombre_nuevo}

    for nombre_viejo in equipos_a_renombrar:
        # Sacamos una palabra de la lista (asegura no repetición entre ellos)
        palabra = PALABRAS_CLAVE.pop(0)
        nuevo_nombre = f"Game Team {palabra}"

        # Verificación extra: Asegurar que NO exista ni en el Bloque 1
        if nuevo_nombre in equipos_db:
            imprimir_error(f"Conflicto: '{nuevo_nombre}' ya existe. Saltando...")
            continue

        nuevos_nombres_map[nombre_viejo] = nuevo_nombre

    # 5. Aplicar cambios al diccionario principal
    for viejo, nuevo in nuevos_nombres_map.items():
        # Extraemos los datos del equipo viejo
        datos_equipo = equipos_db.pop(viejo)
        # Actualizamos su etiqueta para saber que ya fue procesado
        datos_equipo['origen'] = 'automatico_procesado'
        # Lo insertamos con el nuevo nombre
        equipos_db[nuevo] = datos_equipo

        print(f"[*] Cambio: '{viejo}'  --->  \033[1m'{nuevo}'\033[0m")
        cambios_realizados += 1

    if cambios_realizados > 0:
        guardar_cambios_finales()
    else:
        imprimir_aviso("No se realizaron cambios.")

