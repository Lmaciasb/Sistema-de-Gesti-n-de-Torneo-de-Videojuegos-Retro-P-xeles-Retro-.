import json
import os

# ==========================================
# CONFIGURACIÓN
# ==========================================
equipos_db = {}
ARCHIVO_DB = 'torneo_data.json'

# ==========================================
# HERRAMIENTAS VISUALES
# ==========================================
def imprimir_linea_divisoria(caracter="-", longitud=220):
    print(caracter * longitud)

def imprimir_encabezado_reporte(texto):
    print("\n")
    imprimir_linea_divisoria("=", 220)
    print(f" {texto} ".center(220))
    imprimir_linea_divisoria("=", 220)

def cargar_datos():
    global equipos_db
    try:
        with open(ARCHIVO_DB, 'r', encoding='utf-8') as f:
            data = json.load(f)
            equipos_db = data["equipos"]
        return True
    except FileNotFoundError:
        print("\033[91mError: No se encontró la base de datos para generar el reporte.\033[0m")
        return False

# ==========================================
# LÓGICA DE CÁLCULO
# ==========================================
def calcular_metricas_equipo(datos):
    """Calcula todas las métricas necesarias para el reporte."""
    # 1. Puntos
    pts_torneo = datos.get('puntos', 0)
    pts_amistosos = datos.get('puntos_amistosos', 0)
    pts_total = pts_torneo + pts_amistosos

    # 2. Partidos
    pj_torneo = datos.get('partidas_jugadas', 0)
    pj_amistosos = datos.get('partidas_amistosas', 0)
    pj_total = pj_torneo + pj_amistosos

    # 3. Rendimiento / Efectividad
    efectividad = 0.0
    calidad = "N/A"
    if pj_total > 0:
        posibles = pj_total * 3
        efectividad = (pts_total / posibles) * 100

        if efectividad >= 85: calidad = "EXCELENTE"
        elif efectividad >= 60: calidad = "BUENO"
        elif efectividad >= 40: calidad = "REGULAR"
        else: calidad = "BAJO"

    return {
        "pts_torneo": pts_torneo,
        "pts_amist": pts_amistosos,
        "pts_total": pts_total,
        "pj_torneo": pj_torneo,
        "pj_amist": pj_amistosos,
        "pj_total": pj_total,
        "efectividad": efectividad,
        "calidad": calidad
    }

# ==========================================
# GENERACIÓN DEL REPORTE
# ==========================================
def generar_reporte_final():
    if not cargar_datos():
        return

    # 1. Preparar datos
    lista_reporte = []

    for nombre_equipo, datos in equipos_db.items():
        metricas = calcular_metricas_equipo(datos)

        # Origen
        origen_raw = datos.get('origen', 'registro_manual')
        origen_fmt = "AUTO" if "auto" in origen_raw else "REG"

        # Datos Jugadores
        miembros = datos['miembros']

        # Jugador 1
        metricas['j1_nombre'] = miembros[0]['nombre']
        metricas['j1_edad'] = miembros[0]['edad']
        metricas['j1_nivel'] = miembros[0]['nivel']

        # Jugador 2
        metricas['j2_nombre'] = miembros[1]['nombre']
        metricas['j2_edad'] = miembros[1]['edad']
        metricas['j2_nivel'] = miembros[1]['nivel']

        metricas['nombre_equipo'] = nombre_equipo
        metricas['origen'] = origen_fmt

        lista_reporte.append(metricas)

    # 2. Ordenamiento (Ranking Global: Puntos Totales -> Efectividad)
    lista_reporte.sort(key=lambda x: (x['pts_total'], x['efectividad']), reverse=True)

    # 3. Despliegue Visual
    imprimir_encabezado_reporte("REPORTE FINAL CONSOLIDADO")

    # --- DEFINICIÓN DE ANCHOS DE COLUMNA ---
    w_rk = 3
    w_eq = 18
    w_orig = 6  # Aumentado para que quepa "TIPO" (4 chars) con holgura

    # Anchos Jugadores
    w_nom = 30
    w_eda = 5   # Aumentado de 3 a 5 (SOLUCIÓN PRINCIPAL: "EDAD" tiene 4 letras)
    w_niv = 4   # Aumentado de 3 a 4 para mejor estética con "NV"

    # Anchos Estadísticas
    w_stat = 3
    w_stat_g = 4

    w_eff = 6
    w_cal = 9

    # --- ENCABEZADOS ---
    # Fila 1 (Categorías)
    print(
        f" {' ':{w_rk}} | {' ':{w_eq}} | {' ':{w_orig}} | "
        f"{'--- INTEGRANTE 1 ---'.center(w_nom + w_eda + w_niv + 6)} | "
        f"{'--- INTEGRANTE 2 ---'.center(w_nom + w_eda + w_niv + 6)} | "
        f"{'PARTIDOS (PJ)'.center((w_stat*2) + w_stat_g + 6)} | "
        f"{'PUNTOS (PTS)'.center((w_stat*2) + w_stat_g + 6)} | "
        f"{' ':{w_eff}} | {' ':{w_cal}}"
    )

    # Fila 2 (Títulos)
    header_str = (
        f" {'RK'.center(w_rk)} | "
        f"{'EQUIPO'.ljust(w_eq)} | "
        f"{'TIPO'.center(w_orig)} | "
        # Jugador 1
        f"{'NOMBRE COMPLETO'.ljust(w_nom)} | {'EDAD'.center(w_eda)} | {'NV'.center(w_niv)} | "
        # Jugador 2
        f"{'NOMBRE COMPLETO'.ljust(w_nom)} | {'EDAD'.center(w_eda)} | {'NV'.center(w_niv)} | "
        # Partidos
        f"{'T'.center(w_stat)} | {'A'.center(w_stat)} | {'TOT'.center(w_stat_g)} | "
        # Puntos
        f"{'T'.center(w_stat)} | {'A'.center(w_stat)} | {'TOT'.center(w_stat_g)} | "
        # Rendimiento
        f"{'%EF'.center(w_eff)} | {'CALIDAD'.ljust(w_cal)}"
    )

    print(header_str)
    imprimir_linea_divisoria("-", 220)

    # 4. ITERACIÓN DE FILAS
    for i, fila in enumerate(lista_reporte):
        rank = i + 1

        # Colores
        color_cal = ""
        reset = "\033[0m"
        if fila['calidad'] == "EXCELENTE": color_cal = "\033[92m"
        elif fila['calidad'] == "BAJO": color_cal = "\033[91m"

        # Nombres
        j1_nom_vis = fila['j1_nombre']
        j2_nom_vis = fila['j2_nombre']
        eq_nom_vis = (fila['nombre_equipo'][:w_eq-1] + '.') if len(fila['nombre_equipo']) > w_eq else fila['nombre_equipo']

        print(
            f" {str(rank).center(w_rk)} | "
            f"{eq_nom_vis.ljust(w_eq)} | "
            f"{fila['origen'].center(w_orig)} | "
            # Jugador 1
            f"{j1_nom_vis.ljust(w_nom)} | {str(fila['j1_edad']).center(w_eda)} | {str(fila['j1_nivel']).center(w_niv)} | "
            # Jugador 2
            f"{j2_nom_vis.ljust(w_nom)} | {str(fila['j2_edad']).center(w_eda)} | {str(fila['j2_nivel']).center(w_niv)} | "
            # Partidos
            f"{str(fila['pj_torneo']).center(w_stat)} | {str(fila['pj_amist']).center(w_stat)} | \033[1m{str(fila['pj_total']).center(w_stat_g)}\033[0m | "
            # Puntos
            f"{str(fila['pts_torneo']).center(w_stat)} | {str(fila['pts_amist']).center(w_stat)} | \033[1m{str(fila['pts_total']).center(w_stat_g)}\033[0m | "
            # Rendimiento
            f"{color_cal}{f'{fila['efectividad']:.0f}%'.center(w_eff)}{reset} | "
            f"{color_cal}{fila['calidad'].ljust(w_cal)}{reset}"
        )

    imprimir_linea_divisoria("-", 220)

    # Leyenda final
    print(f"Total Equipos: {len(lista_reporte)}")
    print("\nLEYENDA DE COLUMNAS:")
    print(" [NV]: Nivel del Participante (1-5)")
    print(" [T]: Torneo (Competencia)  |  [A]: Amistosos  |  [TOT]: Global (Suma torneo y amistosos)")
    print(" [RK]: Ranking Global basado en Puntos Totales (TOT)")
