def solicitar_nombre_participante_unico(mensaje):
    while True:
        entrada = input(mensaje).strip()

        if entrada.upper() == 'X':
            return None

        if entrada == "":
            imprimir_error("El nombre no puede estar vacío.")
            continue

        partes_nombre = entrada.split()
        if len(partes_nombre) < 3:
            imprimir_error("Debe ingresar el nombre completo (Nombre, Primer Apellido y Segundo Apellido).")
            continue

        entrada = entrada.title()

        # Validamos contra la lista en memoria
        nombre_existente = False
        for p in participantes_db:
            if p['nombre'].lower() == entrada.lower():
                nombre_existente = True
                break

        if nombre_existente:
            imprimir_error("Este participante ya se encuentra registrado en la base de datos.")
            continue

        return entrada

