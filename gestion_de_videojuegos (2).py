def obtener_individuales_disponibles():
    """
    Filtra la base de datos y retorna una lista solo con los
    participantes que NO tienen equipo asignado.
    """
    disponibles = []
    for p in participantes_db:
        # Verificamos si el nombre no está en el conjunto de ocupados
        if p['nombre'] not in jugadores_ocupados:
            disponibles.append(p)
    return disponibles

def generar_nombre_equipo_unico(base="Equipo Auto"):
    """
    Genera un nombre único incremental que no exista en la BD.
    """
    contador = 1
    while True:
        nombre_candidato = f"{base} {contador}"
        if nombre_candidato not in equipos_db:
            return nombre_candidato
        contador += 1

def formar_equipos_automaticamente():
    """
    Distribuye equitativamente a los jugadores sin equipo.
    Estrategia High-Low + Guardado Automático.
    """
    imprimir_titulo("FORMACIÓN AUTOMÁTICA Y EQUILIBRADA DE EQUIPOS")

    # 1. Obtener candidatos
    candidatos = obtener_individuales_disponibles()

    # Validación: Mínimo 2 jugadores libres necesarios
    if len(candidatos) < 2:
        imprimir_error("No hay suficientes jugadores individuales (mínimo 2) para formar equipos.")
        return

    # 2. Ordenar candidatos por nivel (Descendente: Mayor a Menor)
    candidatos_ordenados = sorted(candidatos, key=lambda x: x['nivel'], reverse=True)

    print(f"Se encontraron {len(candidatos_ordenados)} jugadores disponibles.")
    print("Generando equipos equilibrados (Nivel Alto + Nivel Bajo)...")

    equipos_creados = 0

    # Ciclo de emparejamiento
    while len(candidatos_ordenados) >= 2:
        # Tomamos al mejor (inicio) y al de menor nivel (final)
        jugador_top = candidatos_ordenados.pop(0)
        jugador_base = candidatos_ordenados.pop(-1)

        integrantes = [jugador_top, jugador_base]

        # 3. Generar Nombre Único
        nombre_equipo = generar_nombre_equipo_unico()

        # 4. Guardar en Estructuras en Memoria
        equipos_db[nombre_equipo] = {
            'miembros': integrantes,
            'puntos': 0,
            'partidas_jugadas': 0,
            'origen': 'automatico'
        }

        # Actualizar set de ocupados
        for m in integrantes:
            jugadores_ocupados.add(m['nombre'])

        print(f" -> {nombre_equipo} formado: {jugador_top['nombre']} (Nv {jugador_top['nivel']}) + {jugador_base['nombre']} (Nv {jugador_base['nivel']})")
        equipos_creados += 1

    if len(candidatos_ordenados) == 1:
        imprimir_aviso(f"El jugador {candidatos_ordenados[0]['nombre']} quedó sin pareja (número impar).")

    # --- INTEGRACIÓN: GUARDADO AUTOMÁTICO ---
    if equipos_creados > 0:
        print("-" * 40)
        imprimir_exito(f"Proceso finalizado. Se crearon {equipos_creados} equipos nuevos.")
        guardar_cambios() # <--- ¡AQUÍ GUARDAMOS LOS CAMBIOS PARA EL BLOQUE 3!
    else:
        imprimir_aviso("No se formaron equipos nuevos.")

def mostrar_equipos_registrados():
    """
    Muestra la lista completa de equipos.
    """
    imprimir_titulo("LISTADO OFICIAL DE EQUIPOS")

    if not equipos_db:
        imprimir_error("Aún no hay equipos registrados en el sistema.")
        return

    for nombre_eq, datos in equipos_db.items():
        # Formateamos los nombres de los miembros con su nivel
        info_miembros = [f"{m['nombre']} (Nv{m['nivel']})" for m in datos['miembros']]
        miembros_str = ", ".join(info_miembros)

        # Calculamos promedio
        niveles = [m['nivel'] for m in datos['miembros']]
        promedio = sum(niveles) / len(niveles)

        print(f"[*] {nombre_eq}")
        print(f"    Integrantes: {miembros_str}")
        print(f"    Promedio Nivel: {promedio:.1f} | Puntos: {datos['puntos']}")
        print("-" * 40)

